#include "Math.h"

int Math::Add(int a, int b){
    return a + b;
}

int Math::Add(int a, int b, int c){
    return a + b + c;
}

int Math::Add(double a, double b){
    return a + b;
}

int Math::Add(double a, double b, double c){
    return a + b + c;
}

int Math::Mul(int a, int b){
    return a * b;
}

int Math::Mul(int a, int b, int c){
    return a * b * c;
}